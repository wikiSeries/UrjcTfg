package App.Model.TvMaze;

public class Rating {
	private double average;
	
	public Rating() {
		super();
	}
	
	public Rating(double average) {
		super();
		this.average = average;
	}

	public double getAverage() {
		return average;
	}

	public void setAverage(double average) {
		this.average = average;
	}
	
	
}
